<?php

	/* ********************************************************
	 * ********************************************************
	 * ********************************************************/
	class LogHelper {

        const MESSAGES  		= "messages";
        const WARNINGS  		= "warnings";
        const ERRORS    		= "errors";
		const CONFIRMATIONS 	= "confirmations";

        public static $messages 		= [];
        public static $warnings 		= [];
        public static $errors   		= [];
		public static $confirmations 	= [];

        /* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public static function addMessage($input) {
            self::$messages[] = $input;
		}

        /* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public static function getMessages() {
            return self::$messages;
		}

        /* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public static function addWarning($input) {
            self::$warnings[] = $input;
		}

        /* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public static function getWarnings() {
            return self::$warnings;
		}

        /* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public static function addError($input) {
            self::$errors[] = $input;
		}

        /* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public static function getErrors() {
            return self::$errors;
		}

		/* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public static function addConfirmation($input) {
            self::$confirmations[] = $input;
		}

        /* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public static function getConfirmations() {
            return self::$confirmations;
		}

        /* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public static function getLogs() {
            return [
                self::MESSAGES  	=> self::getMessages(),
                self::WARNINGS  	=> self::getWarnings(),
                self::ERRORS    	=> self::getErrors(),
				self::CONFIRMATIONS => self::getConfirmations()
            ];
		}

    }

?>